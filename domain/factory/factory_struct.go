// ParkingAssignment maps a bot to its parking zone and grid slot.
// Computed once per tick by PrecomputeParkingSlots, used by botWander for O(1) lookup.
type ParkingAssignment struct {
	ZoneIdx int
	SlotIdx int // grid position within zone
}

// PrecomputeParkingSlots iterates all idle bots once and assigns each to the
// nearest parking zone with a grid slot index. This replaces the O(n*idle_bots)
// inner loop that was previously inside botWander.
func PrecomputeParkingSlots(fs *FactoryState) {
	// Grow or reset the per-bot slice
	for len(fs.BotParkingSlot) < len(fs.Bots) {
		fs.BotParkingSlot = append(fs.BotParkingSlot, ParkingAssignment{})
	}
	if len(fs.BotParkingSlot) > len(fs.Bots) {
		fs.BotParkingSlot = fs.BotParkingSlot[:len(fs.Bots)]
	}

	if len(fs.ParkingZones) == 0 {
		return
	}

	// Count how many idle bots are assigned to each zone
	zoneCounts := make([]int, len(fs.ParkingZones))

	for i := range fs.Bots {
		bot := &fs.Bots[i]
		// Only compute for idle, on-shift bots (same condition as botWander callers)
		isIdle := bot.State == BotIdle
		onShift := i < len(fs.ShiftOnDuty) && fs.ShiftOnDuty[i]
		if !isIdle || !onShift {
			fs.BotParkingSlot[i] = ParkingAssignment{ZoneIdx: 0, SlotIdx: 0}
			continue
		}

		// Find nearest parking zone
		bestZoneIdx := 0
		bestDist := 1e18
		for zi, pz := range fs.ParkingZones {
			dx := pz[0] - bot.X
			dy := pz[1] - bot.Y
			d := dx*dx + dy*dy
			if d < bestDist {
				bestDist = d
				bestZoneIdx = zi
			}
		}

		fs.BotParkingSlot[i] = ParkingAssignment{
			ZoneIdx: bestZoneIdx,
			SlotIdx: zoneCounts[bestZoneIdx],
		}
		zoneCounts[bestZoneIdx]++
	}
}

// FindBotTask returns the task index assigned to the given bot, or -1.
func FindBotTask(fs *FactoryState, botIdx int) int {
	for i := range fs.Tasks.Tasks {
		if fs.Tasks.Tasks[i].Assigned == botIdx {
			return i
		}
	}
	return -1
}

// FactoryStats tracks real-time throughput, efficiency, and KPI metrics.
// Updated each tick by UpdateStats and displayed on the factory dashboard.
type FactoryStats struct {
	// Throughput counters (cumulative, never reset)
	PartsProcessed int // total parts picked up and delivered by bots
	TrucksUnloaded int // inbound trucks fully unloaded
	TrucksLoaded   int // outbound trucks fully loaded and dispatched

	// Real-time bot status snapshot (recomputed each tick)
	BotsCharging  int // bots currently docked at a charging station
	BotsRepairing int // bots currently at the repair workshop
	BotsIdle      int // bots with no task, wandering or parked
	BotsWorking   int // bots actively executing a task (moving to source/dest)
	BotsOffShift  int // bots in off-duty state (not available for work)

	// Delivery performance
	AvgDeliveryTime float64    // rolling average ticks per delivery
	MachineUtil     [4]float64 // per-machine utilization ratio (0.0-1.0), first 4 machines

	// KPI tracking (cumulative)
	MachineUptime   [6]int // total ticks each of the 6 machines spent actively processing
	TotalCycleTime  int    // sum of all individual part cycle times (entry to shipment)
	CompletedCycles int    // parts that completed the full pipeline and were shipped
	DefectCount     int    // parts flagged as defective by QC-Final inspection
	GoodParts       int    // non-defective parts produced
	TotalParts      int    // total parts produced (GoodParts + DefectCount)
	WIP             int    // work-in-progress: parts currently being transported
	IncomingRejects int    // parts rejected at incoming QC before entering production

	// Leaderboard (Feature 12)
	TopWorkers [3][2]int // top-3 most productive bots: [rank][0]=bot index, [rank][1]=delivery count
}

// NewFactoryState creates and initializes a factory world.
func NewFactoryState(botCount int) *FactoryState {
	if botCount <= 0 {
		botCount = DefaultBotCount
	}

	fs := &FactoryState{
		BotCount:    botCount,
		CamX:        WorldW / 2,
		CamY:        WorldH / 2,
		CamZoom:     0.5,
		Speed:       1.0,
		Rng:         rand.New(rand.NewSource(42)),
		SelectedBot:  -1,
		FollowCamBot: -1,
		HoverBot:     -1,

		// Weather
		Weather:      WeatherClear,
		WeatherTimer: 5000,

		// Heatmap
		HeatmapW: 60,
		HeatmapH: 40,
		HeatmapGrid: make([]int, 60*40),

		// Throughput
		ThroughputHistory: make([]int, 100),

		// Selected bot path
		SelectedBotPath: make([][2]float64, 100),
	}

	// Build layout
	fs.buildLayout()

	// Build spatial hash for wall obstacles (fast collision lookup)
	fs.ObsHash = physics.NewSpatialHash(WorldW, WorldH, 100)
	for i, wall := range fs.Walls {
		cx := wall.X + wall.W/2
		cy := wall.Y + wall.H/2
		fs.ObsHash.Insert(i, cx, cy)
	}

	// Spawn bots inside the hall
	fs.Bots = make([]swarm.SwarmBot, botCount)
	for i := range fs.Bots {
		fs.Bots[i] = swarm.SwarmBot{
			X:      HallX + 50 + fs.Rng.Float64()*(HallW-100),
			Y:      HallY + 50 + fs.Rng.Float64()*(HallH-100),
			Speed:  FactoryBotSpeed,
			Angle:  fs.Rng.Float64() * 6.283,
			Energy: 100,
			CarryingPkg: -1,
		}
	}

	// Init per-bot factory state
	fs.Malfunctioning = make([]bool, botCount)
	fs.ShiftOnDuty = make([]bool, botCount)
	fs.ChargerBusyDelay = make([]int, botCount)
	fs.BotRoles = make([]BotRole, botCount)
	fs.BotOpHours = make([]int, botCount)
	fs.BotDeliveries = make([]int, botCount)
	fs.HonkFlash = make([]int, botCount)
	fs.BotCounters = make([]int, botCount)
	fs.BotPallet = make([][]int, botCount)
	fs.TruckTimer = 200 // first truck arrives quickly
	fs.ShiftTimer = 5000 // first shift change at 5000 ticks
	fs.PartEntryTick = make(map[int]int)
	fs.OrderTimer = 500 // first order arrives soon

	// Multi-step production recipes:
	// Recipe 0 (Red parts): CNC-1 (M0) -> Assembly (M2) -> QC-Final (M5)
	// Recipe 1 (Blue parts): CNC-2 (M1) -> Drill-1 (M3) -> QC-Final (M5)
	// Recipe 2 (Yellow parts): CNC-1 (M0) -> Drill-2 (M4) -> QC-Final (M5)
	fs.Recipes = []ProductionRecipe{
		{Steps: []int{0, 2, 5}, InputColor: 1},
		{Steps: []int{1, 3, 5}, InputColor: 2},
		{Steps: []int{0, 4, 5}, InputColor: 3},
	}

	// Initialize bot roles: 60% Transporter, 25% Forklift, 15% Express
	for i := 0; i < botCount; i++ {
		pct := float64(i) / float64(botCount)
		if pct < 0.60 {
			fs.BotRoles[i] = RoleTransporter
		} else if pct < 0.85 {
			fs.BotRoles[i] = RoleForklift
			fs.Bots[i].Speed = 2.0
			fs.Bots[i].Energy = 120
		} else {
			fs.BotRoles[i] = RoleExpress
			fs.Bots[i].Speed = 5.0
			fs.Bots[i].Energy = 60
		}
	}

	// Initialize parking zones (one in receiving area, one in shipping area)
	fs.ParkingZones = [][2]float64{
		{ZoneReceivingX + ZoneReceivingW/2, HallY + HallH - 200},
		{ZoneShippingX + ZoneShippingW/2, HallY + HallH - 200},
	}

	// Initialize shift: 70% on duty, 30% off
	for i := 0; i < botCount; i++ {
		fs.ShiftOnDuty[i] = i < botCount*7/10
	}

	// Feature 7: Inventory tracking defaults
	fs.MinStockLevel = 5

	// Feature 9: Energy economics defaults
	fs.Budget = 10000.0
	fs.EnergyCostDay = 0.1
	fs.EnergyCostNight = 0.03

	// Forklift battery swap timers
	fs.BatterySwapTimer = make([]int, botCount)

	// Material costs + revenue defaults
	fs.MaterialCostPerPart = 50.0
	fs.RevenuePerProduct = 200.0

	// Feature 11: Random events init
	fs.EventTimer = 5000 + fs.Rng.Intn(3000) // first event in 5000-8000 ticks
	fs.EventBreakdownMachine = -1

	// Init achievements
	fs.FactoryAchievements = make([]string, 0)

	// Init spatial hash
	fs.BotHash = physics.NewSpatialHash(WorldW, WorldH, 50)

	return fs
}
